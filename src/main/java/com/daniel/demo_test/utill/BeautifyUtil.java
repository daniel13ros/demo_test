public class BeautifyUtil {
    public static void title(String title) {
        System.out.println("");
        System.out.println("@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@  " + title.toUpperCase() + "  @@@@@@@@@@@@@@@@@@@@@@@@@@@@@@");
        System.out.println("");
    }

    public static void headLine(String title) {
        System.out.println("");
        System.out.println("************************************************************    " + title.toUpperCase() + "    ************************************************************");
        System.out.println("");
    }
}
